//     2. Write a Java program to print the result of the following operations:
//    - `-15 + 58 * 45`
//    - `(35 + 8) % 6`
//    - `24 + -5 * 3 / 7`
//    - `15 + 18 / 3 * 2 - 9 % 3`

public class Second_Instruction {
   public static void main(String[] args) {
        
        System.out.println("-15 + 58 * 45 = "+ (-15 + 58 * 45));
        System.out.println("(35 + 8) % 6 = "+ ((35 + 8) % 6));
        System.out.println("24 + -5 * 3 / 7 = "+ (24 + -5 * 3 / 7));
        System.out.println("15 + 18 / 3 * 2 - 9 % 3 = "+ (15 + 18 / 3 * 2 - 9 % 3));
    }

}
